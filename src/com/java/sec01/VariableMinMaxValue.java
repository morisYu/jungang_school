package com.java.sec01;

public class VariableMinMaxValue {
	public static void main(String[] args) {
		System.out.println("byte 타입의 최소값 : " + Byte.MIN_VALUE + "\t" + "byte 타입의 최대값 : " + Byte.MAX_VALUE);
		System.out.println("short 타입의 최소값 : " + Short.MIN_VALUE + "\t" + "short 타입의 최대값 : " + Short.MAX_VALUE);
		System.out.println("int 타입의 최소값 : " + Integer.MIN_VALUE + "\t" + "int 타입의 최대값 : " + Integer.MAX_VALUE);
		System.out.println("long 타입의 최소값 : " + Long.MIN_VALUE + "\t" + "long 타입의 최대값 : " + Long.MAX_VALUE);
		System.out.println("float 타입의 최소값 : " + Float.MIN_VALUE + "\t" + "float 타입의 최대값 : " + Float.MAX_VALUE);
		System.out.println("double 타입의 최소값 : " + Double.MIN_VALUE + "\t" + "double 타입의 최대값 : " + Double.MAX_VALUE);

	}
}
