package com.java.sec01;

public class VariableSize {
	public static void main(String[] args) {
		System.out.println("byte변수의 메모리크기: "+Byte.BYTES+" byte");
		System.out.println("short변수의 메모리크기: "+Short.BYTES+" byte");
		System.out.println("int변수의 메모리크기: "+Integer.BYTES+" byte");
		System.out.println("long변수의 메모리크기: "+Long.BYTES+" byte");
		System.out.println("float변수의 메모리크기: "+Float.BYTES+" byte");
		System.out.println("double변수의 메모리크기: "+Double.BYTES+" byte");
		System.out.println("char변수의 메모리크기: "+Character.BYTES+" byte");
	}
}
